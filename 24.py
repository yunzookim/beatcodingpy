num1=1.23456789 
num2=0.22222222 
num3=1.87654321
num4=0.21314151

print("First: {:.2}".format(num1)) # 유효숫자는 2개로 출력
print("Second: {:10.4}".format(num2)) # 폭이 10(10개의 글자가 들어갈 수 있는 공간), 유효숫자는 4개로 출력
print("Third: {}".format(num3)) # 전체 출력
print("Fourth: {:10.4}".format(num4)) # 폭이 10(10개의 글자가 들어갈 수 있는 공간), 유효숫자는 4개로 출력